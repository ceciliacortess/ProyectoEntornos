    private void conciertosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_conciertosItemStateChanged
        // TODO add your handling code here:
        if (conciertos.getSelectedItem() == "Bad Gyal Concert") {
            precioNormal.setText("85€");
            precioVIP.setText("100€");
        } else if (conciertos.getSelectedItem() == "Medusa Festival") {
            precioNormal.setText("60€");
            precioVIP.setText("200€");
        } else if (conciertos.getSelectedItem() == "Zevra Festival") {
            precioNormal.setText("90€");
            precioVIP.setText("220€");
        } else if (conciertos.getSelectedItem() == "Rauw Alejandro Concert") {
            precioNormal.setText("100€");
            precioVIP.setText("500€");
        }
    }//GEN-LAST:event_conciertosItemStateChanged

