    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
        if (!rbENormal.isSelected() && !rbEVIP.isSelected()) {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado el tipo de entrada", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String concierto = (String) conciertos.getSelectedItem();
            String entrada = "";
            String precio = "";
            if (rbENormal.isSelected()) {
                entrada = "Entrada normal";
                precio = precioNormal.getText();
            }
            if (rbEVIP.isSelected()) {
                entrada = "Entrada VIP";
                precio = precioVIP.getText();
            }
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Confirma los datos introducidos?\n"
                    + concierto + "\n" + entrada + "\nPrecio: " + precio,
                    "Select an Option", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirmacion == JOptionPane.YES_OPTION) {
                Datos ventana = new Datos();
                ventana.setVisible(true);
                this.dispose();
            }
        }

    }//GEN-LAST:event_nextActionPerformed

